package com.xinxin.command;

public class LightOnCommand implements Command{

    LightReceiver lightReceiver;

    LightOnCommand(LightReceiver lightReceiver) {
        this.lightReceiver = lightReceiver;
    }

    @Override
    public void execute() {
        lightReceiver.on();
    }

    @Override
    public void undo() {
        lightReceiver.off();
    }
}
